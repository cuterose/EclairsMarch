package javaprograms;

public class Add_sub_multiply {

	public static void main(String[] args) {
		int var1 = 25;
		int var2 =5;
		System.out.println("The addition of var1 and var2 is "+(var1+var2));
		System.out.println("The subtraction of var1 and var2 is "+(var1-var2));
		System.out.println("The Multiplication of var1 and var2 is "+(var1*var2));
		System.out.println("The Division of var1 and var2 is "+(var1/var2));
		System.out.println("The Modulus(remainder) of var1 and var2 is "+(var1%var2));

	}

}
